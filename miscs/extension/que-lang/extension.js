const vscode = require("vscode");

/**
 * @param {vscode.ExtensionContext} context
 */
function activate(context) {
  // 1. Clean the data (Removes numbers from signatures like T14 -> T)
  const cleanedLibrary = standardLibrary.map(([name, signature]) => {
    return {
      name,
      signature: signature.replace(/\d+/g, ""),
    };
  });

  // 2. Turn cleaned data into VS Code Completion Items
  const items = cleanedLibrary.map(({ name, signature }) => {
    const item = new vscode.CompletionItem(name);

    // Set icon: Function icon if it contains "->", otherwise Constant icon
    item.kind = signature.includes("->")
      ? vscode.CompletionItemKind.Function
      : vscode.CompletionItemKind.Constant;

    item.detail = signature;
    item.insertText = new vscode.SnippetString(`${name}`);

    return item;
  });

  // 3. Register the provider
  const provider = vscode.languages.registerCompletionItemProvider("que", {
    provideCompletionItems() {
      return items;
    },
  });

  context.subscriptions.push(provider);
}

// VS Code calls this when your extension is shut down
function deactivate() {}

module.exports = {
  activate,
  deactivate,
};
